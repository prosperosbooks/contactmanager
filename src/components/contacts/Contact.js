import React, { Component } from "react";
import { Link } from "react-router-dom";
import PropTypes from "prop-types";
import { Consumer } from "../../context";
import axios from "axios";

class Contact extends Component {
  state = {
    showContactInfo: false
  };

  // create new function that calls prop function from Contacts which calls event listener from Contacts
  onDeleteClick = async (id, dispatch) => {
    try {
      await axios.delete(`https://jsonplaceholder.typicode.com/users/${id}`);
      dispatch({ type: "DELETE_CONTACT", payload: id });
    } catch (e) {
      dispatch({ type: "DELETE_CONTACT", payload: id });
    }
  };

  render() {
    // retrieve ANOTHER component state values
    const { id, name, email, phone } = this.props.retrieveContact;
    // retrieve THIS components state values
    const { showContactInfo } = this.state;

    return (
      <Consumer>
        {value => {
          const { dispatch } = value;
          return (
            <div className="card card-body mb-3">
              <h4>
                {name}
                <i
                  // toggle this components state value opposite to itself
                  onClick={() =>
                    this.setState({
                      showContactInfo: !this.state.showContactInfo
                    })
                  }
                  className="fas fa-sort-down"
                  style={{ cursor: "pointer" }}
                />
                <i
                  className="fas fa-times"
                  style={{ cursor: "pointer", float: "right", color: "red" }}
                  onClick={this.onDeleteClick.bind(this, id, dispatch)} // assign onClick listener to call onDeleteClick which calls deleteClickHandler prop from parent component

                  // click -> onClick (Contact) -> this.onDeleteClick (onClick) -> onDeleteClick (Contact) -> this.props.deleteClickHandler (ContactS) -> deleteClickHandler (ContactS) -> this.deleteContact (deleteClickHandler) -> deleteContact (ContactS) -> response to click
                />

                <Link to={`contact/edit/${id}`}>
                  <i
                    className="fas fa-pencil-alt"
                    style={{
                      cursor: "pointer",
                      float: "right",
                      color: "black",
                      marginRight: "1rem"
                    }}
                  />
                </Link>
              </h4>
              {showContactInfo ? (
                <ul className="list-group">
                  <li className="list-group-item">Name: {name}</li>
                  <li className="list-group-item">Email: {email}</li>
                  <li className="list-group-item">Phone: {phone}</li>
                </ul>
              ) : null}
            </div>
          );
        }}
      </Consumer>
      // style your final front-end display in jsx html with bootstrap here
    );
  }
}

Contact.propTypes = {
  retrieveContact: PropTypes.object.isRequired
};

export default Contact;
